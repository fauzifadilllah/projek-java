          st = Cn.createStatement();
          st.executeUpdate("insert into tb_input values('" + tx1.getText() + "','" + jk.getSelectedItem() + "','" + ju.getSelectedItem() + "','"+ tx4.getText() +"','" + tx44.getText() + "','" + tx5.getText() + "','" + tx6.getText() + "')");
