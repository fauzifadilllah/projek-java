    private void nm_cariKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_nm_cariKeyReleased
        // TODO add your handling code here:
         try{
           st = Cn.createStatement();
           tabmodel.getDataVector().removeAllElements();
           tabmodel.fireTableDataChanged();
           rs=st.executeQuery("select * from tb_input where nama like '%"+ nm_cari.getText()+"%' or kelas like'%"+
                   nm_cari.getText() + "%'or namabuku like'%" + nm_cari.getText()+"%'or durasi like'%"+nm_cari.getText() +"%'or durasi1 like'%" + nm_cari.getText()+"%'or komen like'%" +
                   nm_cari.getText()+ "%' or respon like'%" + nm_cari.getText() + "%'");
           while (rs.next()){
               Object[] data={
                     rs.getString("nama"),
                rs.getString("kelas"),
                rs.getString("namabuku"),
                rs.getString("durasi"),
                rs.getString("durasi1"),
                rs.getString("komen"),
                rs.getString("respon")};
                
              
              tabmodel.addRow(data);
               
           }
        }catch(Exception e){
            e.printStackTrace();
        }

    }//GEN-LAST:event_nm_cariKeyReleased

