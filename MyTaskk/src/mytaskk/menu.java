    private void combo(){
    try{
    String sql = "select * from tb_judul";
    ps = Cn.prepareStatement(sql);
    rs = ps.executeQuery();
    
    while (rs.next()){
        String nama = rs.getString("judul");
        ju.addItem(nama);
    }
    }catch(Exception e){
        JOptionPane.showMessageDialog(null, e);
    }
    }
    
    public void combob(){
        try{
            String sql = "Select * from tb_kelas";
            ps = Cn.prepareStatement(sql);
            rs = ps.executeQuery();
            
            while(rs.next()){
                String nama = rs.getString("kelas");
                jk.addItem(nama);
                
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }
    public void tampilData(){
        try{
          st = Cn.createStatement();
         tabmodel.getDataVector().removeAllElements();
          tabmodel.fireTableDataChanged();
          rs = st.executeQuery("Select * from tb_input");
          while (rs.next()){
          Object[] data ={
              rs.getString("nama"),
              rs.getString("kelas"),
              rs.getString("namabuku"),
              rs.getString("durasi"),
              rs.getString("durasi1"),
              rs.getString("komen"),
              rs.getString("respon")
          };
          tabmodel.addRow(data);
          }
        }catch(Exception e){
        e.printStackTrace();}
    }
