    private void bdeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bdeleteActionPerformed
        // TODO add your handling code here:
         if(tx1.getText().equals("")||jk.getSelectedItem().equals("")||ju.getSelectedItem().equals("")||tx4.getText().equals("")||tx44.getText().equals("")||tx5.getText().equals("")||tx6.getText().equals("")){
         JOptionPane.showMessageDialog(null, "Lengkapi data!!","Kesalahan",JOptionPane.ERROR_MESSAGE);
         return;
        }else
         {
             try{
            int jawab;
            if((jawab = JOptionPane.showConfirmDialog(null,"Menghapus??","Konfirmasi", JOptionPane.YES_NO_OPTION)) == 0){
            st = Cn.createStatement();
            st.executeUpdate("delete from tb_input where " + "nama='" + tabmodel.getValueAt(tdata.getSelectedRow(), 0) + "'");
            tampilData();
            }
            clear();
            bdelete.setEnabled(false);
        }catch(Exception e){
        e.printStackTrace();}
    }//GEN-LAST:event_bdeleteActionPerformed
    }
    private void beditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_beditActionPerformed
        // TODO add your handling code here:
         try{
           st = Cn.createStatement();
           st.executeUpdate("update tb_input set " + "nama='" + tx1.getText() + "'," + "kelas='" + jk.getSelectedItem()+"',"+
                    "namabuku='" + ju.getSelectedItem() + "'," + "durasi='" + tx4.getText() + "'," + "durasi1='" + tx44.getText() + "'," + "komen='" + tx5.getText() + "'," + "respon='" + tx6.getText() + "' where nama='"+tabmodel.getValueAt(tdata.getSelectedRow(),0)+"';");
            tampilData();
            clear();
          
            JOptionPane.showMessageDialog(null,"Berhasil!!");
       } catch(Exception e){
          e.printStackTrace();
       }
    }//GEN-LAST:event_beditActionPerformed

